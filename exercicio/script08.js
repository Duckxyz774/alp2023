let n1 = Number(prompt("Digite um número"));
let n2 = Number(prompt("Digite um número"));

soma = n1+n2 ;
sub = n1-n2 ; 
multi = n1*n2 ;
div = n1/n2 ;

document.write(
    `A soma dos dois números é : ${soma}<br>
     A subtração dos dois é números : ${sub}<br>
     A multiplicação dos dois números é : ${multi}<br>
     A divisão dos dois números é : ${div}<br>`)